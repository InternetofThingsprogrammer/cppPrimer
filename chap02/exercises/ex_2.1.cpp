//练习 2.1：类型 int、long、long long 和 short 的区别是什么？
//无符号类型和带符号类型的区别是什么？float 和 double 类型的区别是什么？

/*
* int、long、long long 和 short 的区别是，这些类型的数据在机器中所占的比特数不同，
* 因而能够表示的整数的范围也不同。
*/

/*
* 无符号类型和带符号类型的区别：
* 无符号类型只能够表示大于等于 0 的值，而带符号类型不仅可以表示大于等于 0 的值，还能表示小于 0 的值。
*/

/*
* float 和 double 类型的区别：
* 首先两个类型能够表示的数据的范围不同，其次 double 类型能够表示的数据的精度要高于 float 类型。
*/